    public void vectorTani(int table3, int table4) {
        Vector v = new Vector();
        v.add(Pengairan.getSelectedItem().toString());
        v.add(KeadaanHasil.getSelectedItem().toString());
        v.add(JumlahJiwa.getText());
        v.add(table3);
        v.add(table4);
        DefaultTableModel dt = (DefaultTableModel) TableTani.getModel();
        dt.addRow(v);
    }

    public void vectorPertanian(int a, int b) {
        int zakatAir;
        int zakatAir1;
        switch (Pengairan.getSelectedIndex()) {
            case 0:
                zakatAir = a * 5 / 100;
                zakatAir1 = zakatAir * b;
                vectorTani(zakatAir, zakatAir1);
                JOptionPane.showMessageDialog(this, "Penghasilan melebihi nishob maka haurs membayar zakat Rp " + zakatAir1);
                break;
            case 1:
                zakatAir = a * 10 / 100;
                zakatAir1 = zakatAir * b;
                vectorTani(zakatAir, zakatAir1);
                JOptionPane.showMessageDialog(this, "Penghasilan melebihi nishob maka haurs membayar zakat Rp " + zakatAir1);
                break;
            case 2:
                zakatAir = a * 75 / 1000;
                zakatAir1 = zakatAir * b;
                vectorTani(zakatAir, zakatAir1);
                JOptionPane.showMessageDialog(this, "Penghasilan melebihi nishob maka haurs membayar zakat Rp " + zakatAir1);
                break;
        }
    }

