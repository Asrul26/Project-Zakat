    private void HitungFtrh1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_HitungFtrh1MouseClicked
        // TODO add your handling code here:

        if (Pengairan.getSelectedItem().toString().isEmpty() || JumlahJiwa.getText().isEmpty() || KeadaanHasil.getSelectedItem().toString().isEmpty() || JumlahJiwa2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select Product and Enter Qty");
        } else {
            int hasilTani = Integer.valueOf(JumlahJiwa.getText());
            int hargaTani = Integer.valueOf(JumlahJiwa2.getText());
            switch (KeadaanHasil.getSelectedIndex()) {
                case 0:
                    if (hasilTani < 653) {
                        JOptionPane.showMessageDialog(this, "Tidak wajib membayar zakat, Karena nisab zakat pertanian adalah 5 wasaq = 653 kg beras.");
                    } else {
                        vectorPertanian(hasilTani, hargaTani);
                    }
                    break;
                case 1:
                    if (hasilTani < 1000) {
                        JOptionPane.showMessageDialog(this, "Tidak wajib membayar zakat, karena berat nishob 1 ton");
                    } else {
                        vectorPertanian(hasilTani, hargaTani);
                    }
                    break;
            }
        }

    }//GEN-LAST:event_HitungFtrh1MouseClicked

